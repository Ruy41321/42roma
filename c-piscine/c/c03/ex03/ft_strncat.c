char	*ft_strncat(char *dest, char *src, unsigned int n)
{
	char			*ptr_dest;
	unsigned int	i;

	i = 0;
	ptr_dest = dest;
	while (*dest != '\0')
		dest++;
	while (*src != '\0' && i < n)
	{
		*dest = *src;
		dest++;
		src++;
		i++;
	}
	*dest = '\0';
	return (ptr_dest);
}
